module.exports = class DepthCalculator {
    calculateDepth(arr, resultArray = [], counter = 1) {

        if (resultArray.length == 0)
            resultArray.push(counter);
    
        arr.forEach(element => {
            if (Object.prototype.toString.apply(element) == '[object Array]') {
                resultArray.push(counter + 1);
                this.calculateDepth(element, resultArray, counter + 1);
            }
        });
    
        return Math.max(...resultArray);        
    }
};















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































